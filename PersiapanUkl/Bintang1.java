package PersiapanUkl;
public class Bintang1 {
    public static void main(String[] args) {
 int j = 3;
        for (int k = 8; k >= 1; k--) {
            for (int d = 1; d < k; d++) {
                System.out.print(j + "  ");
                j += 2;
            }
            System.out.println();
        }
    }
    
}
